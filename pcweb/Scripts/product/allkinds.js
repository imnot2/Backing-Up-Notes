//首页菜单
function parse(data) {
    var str = [], sec, thir,id;
    for (var i = 0, len = data.length; i < len; i++) {
        sec = data[i].kinds;
        id = i == 0 ? 1268 : i == 1 ? 1232 : i == 2 ? 1086 : i == 3 ? 1313 : i == 4 ? 1177 : i == 5 ? 1051 : i;
        str.push('<div id="category' + id + '" class="mod section-' + i + '"><div class="hd"><i class="icon"></i>');
        str.push(data[i].name)
        str.push('</div><div class="clearfix bd">')
        for (j = 0, len1 = sec.length; j < len1; j++) {
            str.push('<dl><dt><a href="' + sec[j].href + '">' + sec[j].name + '</a></dt><dd>');
            thir = sec[j].details;
            for (f = 0, len2 = thir.length; f < len2; f++) {
                str.push('<a href="' + thir[f].href + '" title=' + thir[f].name + '>' + thir[f].name + '</a>');
            }
            str.push('</dd></dl>')
        }
        str.push('</div></div>')
    }
    $('#itemOfKinds').prepend(str.join(""));
}
